def rotate_array(arr)
  arr_copy = []
  arr.each{|e| arr_copy << e.dup}
  arr_copy.push(arr_copy.shift)
  arr_copy
end

def rotate_array2(array)
  array[1..-1] + [array[0]]
end

p rotate_array([7, 3, 5, 2, 9, 1]) == [3, 5, 2, 9, 1, 7]
p rotate_array(['a', 'b', 'c']) == ['b', 'c', 'a']
p rotate_array(['a']) == ['a']

p x = [1, 2, 3, 4]
p rotate_array(x) == [2, 3, 4, 1]   # => true
